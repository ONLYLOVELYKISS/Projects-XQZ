#include <stdio.h>
#include <stdlib.h>


int main() {
	int a=0;
	//0的二进制位是                  00000000000000000000000000000000(补码) 
	int b=~a;
	//整数在计算机内存中是以二进制补码的形式存储 
    //0按位取反后是                  11111111111111111111111111111111(补码) 
	//-1得到反码                     11111111111111111111111111111110(反码)
	//符号位不变其它位取反得到原码   10000000000000000000000000000001(原码) 
	printf("%d",b);//打印的是这个数的原码 
	return 0;
}
